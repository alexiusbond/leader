package kg.alex.spt.domain;

import java.io.Serializable;

public class StudentRelative implements Serializable {

    private int id;
    private int student_id;
    private String fullname;
    private String work_place;
    private String phone;
    private String address;
    private String passport;
    private int is_main;
    private int relatives_id;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getStudent_id() {
        return student_id;
    }

    public void setStudent_id(int student_id) {
        this.student_id = student_id;
    }

    public String getFullname() {
        return fullname;
    }

    public void setFullname(String fullname) {
        this.fullname = fullname;
    }

    public String getWork_place() {
        return work_place;
    }

    public void setWork_place(String work_place) {
        this.work_place = work_place;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getPassport() {
        return passport;
    }

    public void setPassport(String passport) {
        this.passport = passport;
    }

    public int getIs_main() {
        return is_main;
    }

    public void setIs_main(int is_main) {
        this.is_main = is_main;
    }

    public int getRelatives_id() {
        return relatives_id;
    }

    public void setRelatives_id(int relatives_id) {
        this.relatives_id = relatives_id;
    }

}
